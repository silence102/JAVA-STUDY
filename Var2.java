package variable;
// int: 숫자 정수(integer)
public class Var2 {

    public static void main(String[] args) {
        int a; // 변수 선언
        a = 20; // 변수 초기화
        /*
        =: 오른쪽에 있는 값을 왼쪽에 저장한다는 뜻.
        (수학에서 뜻하는 값이 같다(equals)와는 다른 뜻이다.)
        */
        System.out.println(a);
        System.out.println(a);
        System.out.println(a);
    }
}
