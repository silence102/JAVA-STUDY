package variable;
// 변수는 이름 그대로 변할 수 있다는 뜻.
/*
    Variable(변수):
    수작업으로 변경하는 과정을 간편하게 하기 위함
    값을 보관해두고 필요할 때 값을 꺼내서 읽을 수 있는 저장소 역할
*/
public class Var1 {

    public static void main(String[] args) {
        System.out.println(20); // 10 -> 20
        System.out.println(20); // 10 -> 20
        System.out.println(20); // 10 -> 20
    }
}
